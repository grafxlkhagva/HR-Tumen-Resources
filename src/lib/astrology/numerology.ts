function reduceToSingleDigit(n: number): number {
    if (n === 11 || n === 22 || n === 33) return n; // Мастер тоонууд
    if (n < 10) return n;
    const sum = String(n).split('').reduce((acc, d) => acc + parseInt(d), 0);
    return reduceToSingleDigit(sum);
}

export function calculateLifePath(year: number, month: number, day: number): LifePathInfo {
    const digits = `${year}${String(month).padStart(2, '0')}${String(day).padStart(2, '0')}`;
    const total = digits.split('').reduce((acc, d) => acc + parseInt(d), 0);
    const lifePathNum = reduceToSingleDigit(total);
    return LIFE_PATHS[lifePathNum] || LIFE_PATHS[1];
}

export function getLifePathCalculation(year: number, month: number, day: number): string {
    const digits = `${year}${String(month).padStart(2, '0')}${String(day).padStart(2, '0')}`;
    const total = digits.split('').reduce((acc, d) => acc + parseInt(d), 0);
    const result = reduceToSingleDigit(total);
    return `${digits.split('').join('+')} = ${total} → ${result}`;
}
