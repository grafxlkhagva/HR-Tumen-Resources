export function getChineseSign(year: number, month: number, day: number): ChineseSign {
    const chineseYear = getChineseYear(year, month, day);
    // (year - 1900) % 12 → 0=Хулгана
    const idx = ((chineseYear - 1900) % 12 + 12) % 12;
    return SIGNS[idx];
}
