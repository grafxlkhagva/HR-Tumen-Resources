function getChineseYear(year: number, month: number, day: number): number {
    const lny = LUNAR_NEW_YEAR[year];
    if (!lny) {
        // Жагсаалтанд байхгүй жилд ойролцоо тооцоо
        return year;
    }
    const [lnyMonth, lnyDay] = lny;
    // Хятад шинэ жилийн өмнө төрсөн бол өмнөх жилийн амьтан
    if (month < lnyMonth || (month === lnyMonth && day < lnyDay)) {
        return year - 1;
    }
    return year;
}

